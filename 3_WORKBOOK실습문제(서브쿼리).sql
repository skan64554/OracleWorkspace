-- 9. 보너스가 없고 직급 코드가 J4이거나 J7인 사원의 이름, 직급 명, 급여 조회(NVL)
SELECT EMP_NAME, JOB_NAME, NVL(SALARY,NULL)
FROM EMPLOYEE E
JOIN JOB J ON (J.JOB_CODE = E.JOB_CODE) 
WHERE BONUS IS NULL
AND E.JOB_CODE IN ('J4','J7');

